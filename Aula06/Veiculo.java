package aula06;

/**
 *
 * @author Instrutor
 */
public class Veiculo {
    
    private String nome;
    private String marca;
    private String cor;
    
    
    public Veiculo(String _nome, String _marca, String _cor) {
        
        this.nome = _nome;
        this.marca = _marca;
        this.cor = _cor;
        
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the marca
     */
    public String getMarca() {
        return marca;
    }

    /**
     * @param marca the marca to set
     */
    public void setMarca(String marca) {
        this.marca = marca;
    }

    /**
     * @return the cor
     */
    public String getCor() {
        return cor;
    }

    /**
     * @param cor the cor to set
     */
    public void setCor(String cor) {
        this.cor = cor;
    }
    
    
    
}
